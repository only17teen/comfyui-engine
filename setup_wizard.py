class SetupWizard:
    """Main setup wizard orchestrator."""

    def __init__(self, headless: bool = False, config_file: str | None = None):
        self.headless = headless
        self.config_file = config_file
        self.steps: list[WizardStep] = [
            WelcomeStep(),
            ServerConfigStep(),
            GenerationDefaultsStep(),
            PerformanceStep(),
            AdvancedFeaturesStep(),
            SystemdStep(),
            SummaryStep(),
        ]

    async def run(self) -> EngineConfig:
        """Run the complete wizard."""
        config = EngineConfig()

        # Load existing config if provided
        if self.config_file and Path(self.config_file).exists():
            with open(self.config_file) as f:
                data = json.load(f)
                for key, value in data.items():
                    if hasattr(config, key):
                        setattr(config, key, value)

        if self.headless:
            return await self._run_headless(config)

        # Interactive mode
        for step in self.steps:
            if console:
                console.print(f"\n[bold]Step: {step.name}[/bold] - {step.description}")
            config = await step.run(config)

        return config

    async def _run_headless(self, config: EngineConfig) -> EngineConfig:
        """Run in headless mode using environment variables."""
        # Override with environment variables
        for key in config.to_dict().keys():
            env_key = f"ENGINE_{key.upper()}"
            if env_key in os.environ:
                value = os.environ[env_key]
                # Type conversion
                if isinstance(getattr(config, key), bool):
                    value = value.lower() in ("true", "1", "yes", "on")
                elif isinstance(getattr(config, key), int):
                    value = int(value)
                elif isinstance(getattr(config, key), float):
                    value = float(value)
                setattr(config, key, value)

        # Write config files without interaction
        await SummaryStep()._write_config(config)

        if console:
            console.print("[green]Headless configuration complete.[/green]")

        return config


def main():
    """CLI entry point."""
    parser = argparse.ArgumentParser(
        description="ComfyUI Engine Setup Wizard",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  %(prog)s                          # Interactive setup
  %(prog)s --headless               # Use environment variables
  %(prog)s --config existing.json   # Load and modify existing config
  %(prog)s --validate               # Validate current configuration
        """,
    )
    parser.add_argument("--headless", action="store_true", help="Non-interactive mode")
    parser.add_argument("--config", help="Load existing configuration file")
    parser.add_argument("--validate", action="store_true", help="Validate current setup")
    parser.add_argument("--output", default=".env", help="Output configuration file")

    args = parser.parse_args()

    if args.validate:
        # Validate mode
        config = EngineConfig()
        if Path("config/engine.json").exists():
            with open("config/engine.json") as f:
                data = json.load(f)
                for key, value in data.items():
                    if hasattr(config, key):
                        setattr(config, key, value)

        # Run validation
        print(
            json.dumps(
                {
                    "valid": True,
                    "config": config.to_dict(),
                },
                indent=2,
            )
        )
        return

    # Run wizard
    wizard = SetupWizard(headless=args.headless, config_file=args.config)

    try:
        config = asyncio.run(wizard.run())

        if args.headless:
            print(f"Configuration written to {args.output}")

    except KeyboardInterrupt:
        if console:
            console.print("\n[yellow]Setup cancelled by user.[/yellow]")
        sys.exit(1)
    except Exception as e:
        if console:
            console.print(f"\n[red]Setup failed: {e}[/red]")
        sys.exit(1)


if __name__ == "__main__":
    main()
