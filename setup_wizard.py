class SystemdStep(WizardStep):
    """Configure systemd service for Arch Linux."""

    def __init__(self):
        super().__init__("Systemd", "Systemd service configuration")

    async def run(self, config: EngineConfig) -> EngineConfig:
        if sys.platform != "linux":
            if console:
                console.print("[yellow]Systemd configuration skipped (not Linux)[/yellow]")
            return config

        if console:
            console.print(Panel("[bold]Systemd Service Setup[/bold]", border_style="cyan"))

            config.systemd_user = Confirm.ask(
                "Install as user service (not system-wide)?",
                default=config.systemd_user,
            )
            config.systemd_instances = IntPrompt.ask(
                "Number of engine instances",
                default=config.systemd_instances,
            )

            if Confirm.ask("Install systemd service now?", default=False):
                await self._install_systemd(config)

        return config

    async def _install_systemd(self, config: EngineConfig) -> None:
        """Install systemd service files."""
        try:
            service_dir = (
                Path.home() / ".config" / "systemd" / "user" if config.systemd_user else Path("/etc/systemd/system")
            )
            service_dir.mkdir(parents=True, exist_ok=True)

            # Copy service template
            source = Path("systemd/comfyui-engine@.service")
            if source.exists():
                dest = service_dir / "comfyui-engine@.service"
                shutil.copy2(source, dest)

                if console:
                    console.print(f"[green]✓ Installed service template to {dest}[/green]")
                    console.print("\nTo start the service:")
                    console.print(f"  systemctl --user enable comfyui-engine@1.service")
                    console.print(f"  systemctl --user start comfyui-engine@1.service")
            else:
                console.print("[yellow]Service template not found. Run install.sh manually.[/yellow]")

        except Exception as e:
            if console:
                console.print(f"[red]Failed to install systemd service: {e}[/red]")


