@dataclass
class EngineConfig:
    """Complete engine configuration generated by the wizard."""

    # ComfyUI Server
    comfyui_host: str = "127.0.0.1"
    comfyui_port: int = 8188
    comfyui_api_prefix: str = "/api"

    # Generation Settings
    default_width: int = 512
    default_height: int = 512
    default_steps: int = 20
    default_cfg: float = 7.0
    default_sampler: str = "euler_ancestral"
    default_scheduler: str = "normal"

    # Performance
    max_concurrent_jobs: int = 4
    queue_poll_interval: float = 1.0
    request_timeout: float = 300.0
    retry_attempts: int = 3
    retry_backoff: float = 2.0

    # Output
    output_dir: str = "output_models"
    save_metadata: bool = True
    metadata_format: str = "json"  # json, yaml, or both
    git_enabled: bool = True
    git_auto_commit: bool = True

    # Advanced Features
    mlflow_enabled: bool = False
    mlflow_tracking_uri: str = ""
    redis_enabled: bool = False
    redis_url: str = "redis://localhost:6379/0"
    metrics_enabled: bool = False
    metrics_port: int = 9090
    websocket_enabled: bool = True
    checkpoint_enabled: bool = True

    # Paths
    prompts_config: str = "config/prompts.yaml"
    log_dir: str = "logs"
    session_dir: str = ".sessions"

    # Systemd
    systemd_user: bool = True
    systemd_instances: int = 1

    def to_dict(self) -> dict[str, Any]:
        return asdict(self)

    def to_json(self, indent: int = 2) -> str:
        return json.dumps(self.to_dict(), indent=indent)

    def to_env_file(self) -> str:
        """Generate .env file content."""
        lines = ["# ComfyUI Engine Configuration", "# Generated by setup wizard", ""]
        for key, value in self.to_dict().items():
            env_key = f"ENGINE_{key.upper()}"
            if isinstance(value, bool):
                value = str(value).lower()
            elif isinstance(value, list):
                value = json.dumps(value)
            lines.append(f"{env_key}={value}")
        return "\n".join(lines)


class WizardStep:
    """Base class for wizard steps."""

    def __init__(self, name: str, description: str):
        self.name = name
        self.description = description

    async def run(self, config: EngineConfig) -> EngineConfig:
        raise NotImplementedError


class WelcomeStep(WizardStep):
    """Welcome and system detection."""

    def __init__(self):
        super().__init__("Welcome", "System detection and welcome")

    async def run(self, config: EngineConfig) -> EngineConfig:
        if console:
            console.print(
                Panel.fit(
                    "[bold blue]ComfyUI Engine Setup Wizard[/bold blue]\n\n"
                    "This wizard will guide you through configuring the ComfyUI Engine\n"
                    "for optimal performance on your system.",
                    title="Welcome",
                    border_style="blue",
                )
            )

            # Detect system
            system = self._detect_system()
            console.print(f"\n[green]Detected OS:[/green] {system['os']}")
            console.print(f"[green]Python:[/green] {system['python']}")
            console.print(f"[green]Architecture:[/green] {system['arch']}")

            if system["os"] == "Linux":
                distro = system.get("distro", "Unknown")
                console.print(f"[green]Distribution:[/green] {distro}")
                if "arch" in distro.lower():
                    console.print("[cyan]✓ Arch Linux detected - full systemd support available[/cyan]")

            console.print()
            Confirm.ask("Press Enter to continue", default=True)

        return config

    def _detect_system(self) -> dict[str, str]:
        """Detect system information."""
        info = {
            "os": sys.platform,
            "python": f"{sys.version_info.major}.{sys.version_info.minor}.{sys.version_info.micro}",
            "arch": os.uname().machine if hasattr(os, "uname") else "unknown",
        }

        if sys.platform == "linux":
            try:
                with open("/etc/os-release") as f:
                    for line in f:
                        if line.startswith("PRETTY_NAME="):
                            info["distro"] = line.split("=")[1].strip().strip('"')
                            break
            except Exception:
                pass

        return info


class ServerConfigStep(WizardStep):
    """Configure ComfyUI server connection."""

    def __init__(self):
        super().__init__("Server", "ComfyUI server configuration")

    async def run(self, config: EngineConfig) -> EngineConfig:
        if console:
            console.print(Panel("[bold]ComfyUI Server Configuration[/bold]", border_style="green"))

            config.comfyui_host = Prompt.ask(
                "Server host",
                default=config.comfyui_host,
            )
            config.comfyui_port = IntPrompt.ask(
                "Server port",
                default=config.comfyui_port,
            )

            # Test connection
            if Confirm.ask("Test connection to ComfyUI server?", default=True):
                await self._test_connection(config)

        return config

    async def _test_connection(self, config: EngineConfig) -> None:
        """Test connection to ComfyUI server."""
        import aiohttp

        url = f"http://{config.comfyui_host}:{config.comfyui_port}{config.comfyui_api_prefix}/system_stats"

        with Progress(
            SpinnerColumn(),
            TextColumn("[progress.description]{task.description}"),
            console=console,
        ) as progress:
            task = progress.add_task("Testing connection...", total=None)

            try:
                async with aiohttp.ClientSession(timeout=aiohttp.ClientTimeout(total=5)) as session, session.get(
                    url
                ) as response:
                    if response.status == 200:
                        data = await response.json()
                        progress.update(task, description="[green]✓ Connection successful![/green]")
                        if console:
                            console.print(f"[green]Server info:[/green] {data}")
                    else:
                        progress.update(task, description=f"[red]✗ Server returned {response.status}[/red]")
            except Exception as e:
                progress.update(task, description=f"[red]✗ Connection failed: {e}[/red]")
                if console:
                    console.print("[yellow]You can continue setup and fix the connection later.[/yellow]")


